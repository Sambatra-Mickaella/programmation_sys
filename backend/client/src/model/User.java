package model;

public class User {
    String nom;
    String password;

    // getters
    public String getNom() {
        return nom;
    }
    public String getPassword() {
        return password;
    }

    // setters
    public void setNom(String nom) {
        this.nom = nom;
    }
    public void setPassword(String password) {
        this.password = password;
    }

}
